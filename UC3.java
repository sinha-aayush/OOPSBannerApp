public class UC3 {
    public static void main(String[] args) {
        // Constructing each line by joining the individual letter segments
        System.out.println(String.join("   ","  ***  ", "   ***  ", "****** ", " ***** "));
        System.out.println(String.join("  "," **  ** " ," **  ** ","**    **","**      "));
        System.out.println(String.join("  ","**    **", "**    **","**    **"," **      "));
        System.out.println(String.join("  ","**    **", "**    **","****** ","   ***   "));
        System.out.println(String.join("  ","**    **", "**    **","**     ","     ** "));
        System.out.println(String.join("  ","**    **", "**    **","**     ","      ** "));
        System.out.println(String.join("  "," **   **", " **  ** ","**     ","     ** "));
        System.out.println(String.join("   ","  ***  ","  ***  ","**     "," *****   "));
    }
}
 